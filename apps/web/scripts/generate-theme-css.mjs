// Codegen: design-token SSOT (@chudbox/shared) -> the web's Tailwind v4 @theme.
// The web theme DERIVES from the TS SSOT; values are never hand-duplicated.
// Run `pnpm gen:theme` (build @chudbox/shared first) and commit the output.
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { tokens } from '@chudbox/shared/tokens'

const OUT = fileURLToPath(new URL('../src/theme.generated.css', import.meta.url))
const kebab = (s) =>
  s
    .replace(/([a-z0-9])([A-Z])/g, '$1-$2')
    .replace(/([a-zA-Z])([0-9])/g, '$1-$2')
    .toLowerCase()

// Every color becomes a :root channel (raw rgb triple, for rgb(var(--x)) use)
// + a Tailwind color (--color-*: rgb(var(--channel)), for utilities + opacity).
const channels = [] // [cssVarName, "r g b"]
const colorUtils = [] // [tailwindColorName, channelVarName]

for (const [k, v] of Object.entries(tokens.themeChannels)) {
  channels.push([kebab(k), v])
  colorUtils.push([kebab(k), kebab(k)]) // bg-accent, text-on-accent, ...
}
for (const [k, v] of Object.entries(tokens.text)) {
  channels.push([`text-${kebab(k)}`, v])
  colorUtils.push([`text-${kebab(k)}`, `text-${kebab(k)}`]) // text-text-primary
}
for (const [role, t] of Object.entries(tokens.status)) {
  channels.push([`status-${role}-bg`, t.bg])
  channels.push([`status-${role}-text`, t.text])
  channels.push([`status-${role}-border`, t.border])
  colorUtils.push([role, `status-${role}-bg`]) // bg-danger
  colorUtils.push([`${role}-fg`, `status-${role}-text`]) // text-danger-fg
  colorUtils.push([`${role}-border`, `status-${role}-border`]) // border-danger-border
}

const px = (n) => `${n}px`
const root = [
  ...channels.map(([n, v]) => `  --${n}: ${v};`),
  ...Object.entries(tokens.iconSize).map(([k, v]) => `  --icon-${kebab(k)}: ${px(v)};`),
].join('\n')

const themeInline = colorUtils.map(([tw, ch]) => `  --color-${tw}: rgb(var(--${ch}));`).join('\n')

const themeStatic = [
  ...Object.entries(tokens.radius).map(([k, v]) => `  --radius-${k}: ${px(v)};`),
  ...Object.entries(tokens.type).flatMap(([k, s]) => [
    `  --text-${k}: ${px(s.size)};`,
    `  --text-${k}--line-height: ${px(s.leading)};`,
  ]),
  `  --font-sans: ${tokens.font.sans};`,
  `  --font-mono: ${tokens.font.mono};`,
  `  --shadow-elevation: ${tokens.elevation};`,
].join('\n')

const css = `/* ---------------------------------------------------------------------------
 * DO NOT EDIT — generated by apps/web/scripts/generate-theme-css.mjs
 * from the @chudbox/shared design-token SSOT (packages/shared/src/tokens.ts).
 * Run \`pnpm gen:theme\` to regenerate. Imported by src/index.css.
 * ------------------------------------------------------------------------- */

/* Raw channels: themes.ts rewrites accent/surface/... at runtime; the rest are
 * static. Referenced directly by hand-written CSS as rgb(var(--name)). */
:root {
${root}
}

/* \`inline\` keeps utilities emitting rgb(var(--channel)) so theme switching AND
 * opacity modifiers (bg-accent/40 -> color-mix) both work. */
@theme inline {
${themeInline}
}

/* Static scales: radius, type (size/leading), fonts, elevation. */
@theme {
${themeStatic}
}
`

writeFileSync(OUT, css)
console.log(`Wrote ${OUT} (${channels.length} channels, ${colorUtils.length} color utils)`)
